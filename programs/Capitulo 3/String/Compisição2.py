# %d = Numeros Inteiros | %s = Strings | %f = Numeros Decimais 

idade = 22 
print("[%d]" % idade)

idade = 22
print("[%03d]" % idade)

idade = 22
print("[%3d]" % idade)

idade = 22 
print("[%-3d]" % idade)

# %d = Numeros Inteiros | %s = Strings | %f = Numeros Decimais 

salario = 2000 
print("[%5.1f]" % salario)

salario = 1500
print("[%5.5f]" % salario)

salario = 1789.76
print("[%2.3f]" % salario)

salario = 1000.50 
print("[%5.2f]" % salario)