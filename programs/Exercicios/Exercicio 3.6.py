materia1 = float(input("Digite a média da primeira matéria: "))
materia2 = float(input("Digite a média da segunda matéria: "))
materia3 = float(input("Digite a média da terceira matéria: "))

media = (materia1+materia2+materia3)/3
if media>=7:
    print("Aprovado")
else:
    print("Reprovado")    
    
